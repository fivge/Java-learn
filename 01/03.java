class s03{
  public static void main(String[] args){
    //数据类型
    int a = 100;
    long b = 88889999777766L;
    long c = 88887777999955l;
    //声明long型必须加L/l，否则会报错

    System.out.println(a);
    System.out.println(b);
    System.out.println(c);

    double d = 10.24;
    float e = 2.5f;
    float f = 5.2F;
    //声明float型必须加f/F,否则会报错

  }
}
